export function getWhatsAppUrl(message = "Olá, gostaria de falar com a DataAcquire.") {
  const number = siteContent.contact.whatsapp;

  if (!number) {
    return "";
  }

  return `https://wa.me/${number}?text=${encodeURIComponent(message)}`;
}
